"""LLM-as-judge evaluator for RAG answer quality.

Scores each Q&A response on three dimensions (1-5 scale):
- Faithfulness: Is the answer grounded in the retrieved context?
- Relevance: Does the answer address the question?
- Completeness: Does the answer cover key points from context?
"""

import json

from pydantic import BaseModel, Field
from langchain_core.language_models.chat_models import BaseChatModel
from langchain_core.prompts import ChatPromptTemplate


class EvalResult(BaseModel):
    """Result of evaluating a single Q&A pair."""

    question: str
    answer: str
    context: str
    faithfulness: int = Field(ge=1, le=5, description="Grounding in context (1-5)")
    relevance: int = Field(ge=1, le=5, description="Addresses the question (1-5)")
    completeness: int = Field(ge=1, le=5, description="Coverage of key points (1-5)")
    overall: float = Field(description="Average of the three metrics")
    reasoning: str = Field(default="", description="LLM's evaluation explanation")


EVAL_PROMPT = ChatPromptTemplate.from_messages([
    (
        "system",
        "You are an expert evaluator for Retrieval-Augmented Generation (RAG) systems. "
        "Your job is to assess the quality of an answer generated by a RAG system.\n\n"
        "You will be given:\n"
        "- A question asked by the user\n"
        "- The context retrieved from the knowledge base\n"
        "- The answer generated by the system\n\n"
        "Rate the answer on each metric using a 1-5 scale:\n\n"
        "1. **Faithfulness**: Is the answer grounded in the provided context? "
        "(5 = fully supported by context, 1 = hallucinated / contradicts context)\n\n"
        "2. **Relevance**: Does the answer actually address the question asked? "
        "(5 = directly answers the question, 1 = completely off-topic)\n\n"
        "3. **Completeness**: Does the answer cover the key information available in the context? "
        "(5 = comprehensive coverage, 1 = misses critical information)\n\n"
        "Respond with ONLY a valid JSON object (no markdown, no explanation outside JSON):\n"
        '{{"faithfulness": N, "relevance": N, "completeness": N, "reasoning": "brief explanation"}}'
    ),
    (
        "human",
        "Question: {question}\n\n"
        "Context (retrieved documents):\n{context}\n\n"
        "Answer (system-generated):\n{answer}"
    ),
])


def evaluate_answer(
    question: str,
    answer: str,
    context: str,
    llm: BaseChatModel,
) -> EvalResult:
    """Use the LLM to judge the quality of a RAG answer.

    Sends a structured evaluation prompt and parses the JSON response
    into an EvalResult with faithfulness, relevance, completeness scores.

    Args:
        question: The original user question.
        answer: The system-generated answer to evaluate.
        context: The retrieved documents used as RAG context.
        llm: The LLM to use as judge.

    Returns:
        EvalResult with scores and reasoning.
    """
    messages = EVAL_PROMPT.format_messages(
        question=question,
        context=context,
        answer=answer,
    )

    response = llm.invoke(messages)
    content = response.content.strip()

    # Parse JSON from response (handle markdown code blocks)
    if content.startswith("```"):
        # Strip ```json ... ``` wrapper
        lines = content.split("\n")
        content = "\n".join(
            line for line in lines
            if not line.strip().startswith("```")
        )

    try:
        scores = json.loads(content)
    except json.JSONDecodeError:
        # If parsing fails, return minimum scores with error reasoning
        return EvalResult(
            question=question,
            answer=answer,
            context=context[:500],
            faithfulness=1,
            relevance=1,
            completeness=1,
            overall=1.0,
            reasoning=f"Failed to parse LLM evaluation response: {content[:200]}",
        )

    faithfulness = max(1, min(5, int(scores.get("faithfulness", 1))))
    relevance = max(1, min(5, int(scores.get("relevance", 1))))
    completeness = max(1, min(5, int(scores.get("completeness", 1))))
    overall = round((faithfulness + relevance + completeness) / 3.0, 2)

    return EvalResult(
        question=question,
        answer=answer,
        context=context[:500],
        faithfulness=faithfulness,
        relevance=relevance,
        completeness=completeness,
        overall=overall,
        reasoning=scores.get("reasoning", ""),
    )
